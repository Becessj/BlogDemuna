          </p>
          <ul type="square justi">
            <li class="justi"><b>Normas de comportamiento:</b> Las normas de comportamiento son reglas o pautas que están orientadas a resguardar los derechos y deberes de las niñas, niños y adolescentes. Estas reglas o pautas pueden ser asumidas por cualquier persona, a título personal o en representación de alguna institución, organización o servicio.</li>
            <li class="justi"><b>Reconocimiento voluntario de filiación extrajudicial:</b> El Acta de Compromisos, el padre o madre reconoce como hijo o hija a la niña, niño o adolescente beneficiario/a del reconocimiento, obligándose a registrar dicho acto ante la Oficina de Registro de identidad correspondiente.</li>
          </ul>

          <div class="title-box-d">
            <h3 class="title-d">f)	Gestiones administrativas</h3>
          </div>
          <p class="color-text-a justi" >
