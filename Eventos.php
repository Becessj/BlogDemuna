            <li class="justi"><b>Régimen de visitas:</b> Es el derecho que tiene la madre o el padre que no vive con sus hijos e hijas de poder mantener el contacto con ellos, lo que se traduce en determinar días y horas de visita, previo acuerdo de las partes.</li>
          </ul>

          <div class="title-box-d">
